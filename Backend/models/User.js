import mongoose from 'mongoose';

const schema = mongoose.Schema;

// this defines the schema(blueprint) for the user model
const userSchema = new Schema({
    name: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

export default mongoose.model('User', userSchema); 